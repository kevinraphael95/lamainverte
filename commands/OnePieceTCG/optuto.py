class TutoView(View):
    def __init__(self, embed_list, message=None):
        super().__init__(timeout=300)
        self.embed_list = embed_list
        self.message = message
        self.index = 0
        self.update_buttons()

    def update_buttons(self):
        self.clear_items()
        self.add_item(Button(label="‚¨ÖÔ∏è Pr√©c√©dent", style=discord.ButtonStyle.blurple, custom_id="prev", disabled=self.index == 0))
        self.add_item(Button(label="‚û°Ô∏è Suivant", style=discord.ButtonStyle.blurple, custom_id="next", disabled=self.index == len(self.embed_list)-1))

    @discord.ui.button(label="‚¨ÖÔ∏è Pr√©c√©dent", style=discord.ButtonStyle.blurple)
    async def prev_button(self, interaction: discord.Interaction, button: discord.ui.Button):
        if self.index > 0:
            self.index -= 1
            self.update_buttons()
            await safe_edit(self.message, embed=self.embed_list[self.index], view=self)

    @discord.ui.button(label="‚û°Ô∏è Suivant", style=discord.ButtonStyle.blurple)
    async def next_button(self, interaction: discord.Interaction, button: discord.ui.Button):
        if self.index < len(self.embed_list) - 1:
            self.index += 1
            self.update_buttons()
            await safe_edit(self.message, embed=self.embed_list[self.index], view=self)

