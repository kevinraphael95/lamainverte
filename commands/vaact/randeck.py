class Randeck(commands.Cog):
    """
    Commande !randeck â€” Tire un deck alÃ©atoire et affiche tous les liens avec des boutons
    """

    def __init__(self, bot: commands.Bot):
        self.bot = bot

    @commands.command(
        name="randeck",
        aliases=["deckroulette"],
        help="Tire un deck custom alÃ©atoire Ã  jouer.",
        description="Choisit un deck alÃ©atoire et affiche tous les liens disponibles avec des boutons."
    )
    async def randeck(self, ctx: commands.Context):
        try:
            data = load_data()
            decks = []

            # Construire une liste de (saison, personnage, niveau, dict liens)
            for saison, persos in data.items():
                for duelliste, infos in persos.items():
                    deck_data = infos.get("deck", {})
                    if not isinstance(deck_data, dict):
                        continue
                    for niveau, liens in deck_data.items():
                        if isinstance(liens, dict) and liens:
                            decks.append((saison, duelliste, niveau, liens))

            if not decks:
                return await safe_send(ctx, "âŒ Aucun deck n'est disponible.")

            # Tirage alÃ©atoire
            saison, duelliste, niveau, liens_dict = random.choice(decks)

            # â”€ Embed stylÃ© â”€
            embed = discord.Embed(
                title="ğŸ² Deck AlÃ©atoire TirÃ© !",
                color=discord.Color.random()
            )
            embed.add_field(
                name="ğŸ‘¤ Duelliste",
                value=f"**{duelliste}** *(Saison : {saison})*",
                inline=False
            )
            embed.add_field(
                name="ğŸšï¸ Niveau",
                value=niveau,
                inline=False
            )

            # â”€â”€ CrÃ©ation des boutons pour tous les liens â”€â”€
            view = discord.ui.View()
            for nom, url in liens_dict.items():
                view.add_item(discord.ui.Button(label=nom, url=url))

            embed.set_footer(
                text=f"TirÃ© par {ctx.author.display_name}",
                icon_url=ctx.author.display_avatar.url
            )

            await safe_send(ctx.channel, embed=embed, view=view)

        except Exception as e:
            print(f"[ERREUR randeck] {e}")
            await safe_send(ctx.channel, "âŒ Une erreur est survenue lors du tirage du deck.")

